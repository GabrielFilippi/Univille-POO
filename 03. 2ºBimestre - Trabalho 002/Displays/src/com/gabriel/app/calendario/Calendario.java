package com.gabriel.app.calendario;

public class Calendario {
	private int dia;
	private int mes;
	private int ano;

	public int getDia() {
		return dia;
	}
	public int getMes() {
		return mes;
	}
	public int getAno() {
		return ano;
	}

	public void setDia(int valor) {
		this.dia = valor;
	}
	
	public void setMes(int valor) {
		this.mes = valor;
	}
	
	public void setAno(int valor) {
		this.ano = valor;
	}
	
}
